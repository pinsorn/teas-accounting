import { walkthrough } from '../lib/walkthrough';
import { showPdfSample } from '../lib/pdf-sample';

walkthrough({
  id: '07.03',
  title: 'ภาษีเงินได้นิติบุคคล — ภ.ง.ด.51 / ภ.ง.ด.50',
  chapter: '7. ภาษี',
  persona: 'admin',
  intro: `
นิติบุคคลเสียภาษีเงินได้จาก **กำไรสุทธิทางภาษี** (ไม่ใช่กำไรทางบัญชีตรง ๆ) โดยยื่น 2 แบบต่อปี:

- **ภ.ง.ด.51 (ครึ่งปี, ม.67ทวิ)** — ประมาณการกำไรทั้งปี เสียภาษีครึ่งหนึ่งล่วงหน้า
  ยื่นภายใน 2 เดือนนับจากวันสิ้นครึ่งรอบบัญชี.
- **ภ.ง.ด.50 (ประจำปี)** — ยื่นภายใน 150 วันนับจากวันสิ้นรอบบัญชี; ภาษีที่จ่ายตาม ภ.ง.ด.51
  และ **เครดิตภาษีถูกหัก ณ ที่จ่าย (50ทวิ ขาเข้า)** นำมาหักออกได้.

ระบบดึงรายได้/รายจ่ายจากบัญชีมาทำ **"บันไดคำนวณ"** — กำไรทางบัญชี → บวกกลับรายจ่ายต้องห้าม
(ม.65ตรี) → หักรายการยกเว้น → **กำไรสุทธิทางภาษี** → ภาษี (อัตรา SME/ทั่วไป) แล้ว
**กรอกแบบ RD ตัวจริงให้อัตโนมัติ**. แดชบอร์ดโชว์ทุกตัวเลขก่อน (dry-run) — รวมงบแสดงฐานะ
การเงินย่อที่ต้อง **ดุล (balanced)** — แล้วกดออก PDF.
  `.trim(),
  prerequisites: [
    'login admin (สิทธิ์ดู CIT/รายงาน)',
    'มีรายการบัญชีในรอบปี (รายได้/รายจ่าย) + เครดิต 50ทวิ (ดู บท 4–5)',
    'รัน manual/render-pdf-samples.py แล้ว (สร้างตัวอย่าง PDF)',
  ],
}, async ({ page, capture }) => {

  // ─── Step 1: CIT dashboard — every figure before generating ──────────
  await page.goto('/tax-filings/cit');
  await page.waitForLoadState('networkidle');
  await page.getByText('ภ.ง.ด.50').first().waitFor({ state: 'visible', timeout: 15_000 });
  await capture('step-01-cit-dashboard', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 1: แดชบอร์ด CIT (/tax-filings/cit) — การ์ด "โปรไฟล์ SME", "บันไดคำนวณ ภ.ง.ด.50"' +
      ' (กำไรทางบัญชี → กำไรสุทธิทางภาษี → ภาษี − เครดิต = สุทธิ), "งบแสดงฐานะการเงินย่อ" พร้อมป้าย' +
      ' ดุล/ไม่ดุล, ตารางเครดิต 50ทวิ ขาเข้า, และส่วนออก PDF ภ.ง.ด.50 (ติ๊กคำรับรองก่อนดาวน์โหลด)',
  });

  // ─── Step 2: ภ.ง.ด.51 — system-filled RD form (half-year) ────────────
  await showPdfSample(page, 'pnd51-p1.png');
  await capture('step-02-pnd51-pdf', {
    caption:
      'ขั้นที่ 2: ตัวอย่าง **ภ.ง.ด.51** (ภาษีกลางปี) ที่ระบบกรอกลงแบบ ของกรมสรรพากรตัวจริงให้' +
      ' — หัวกระดาษ (ชื่อ/เลขผู้เสียภาษี/รอบบัญชี) + ช่องคำนวณภาษีจากประมาณการ พิมพ์ออกยื่นได้',
  });

  // ─── Step 3: ภ.ง.ด.50 — system-filled RD form (annual) ───────────────
  await showPdfSample(page, 'pnd50-p1.png');
  await capture('step-03-pnd50-pdf', {
    caption:
      'ขั้นที่ 3: ตัวอย่าง **ภ.ง.ด.50** (ภาษีประจำปี) หน้าปก ที่ระบบกรอกให้ — แบบเต็มมีหลายหน้า' +
      ' (รายการคำนวณภาษี + งบการเงิน + ใบแนบ) เติมจากบัญชีจริง ตรงกับตัวเลขในแดชบอร์ดขั้นที่ 1',
  });

});
