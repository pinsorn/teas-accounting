/**
 * 03.05 — พนักงาน (master data for payroll)
 *
 * Chapter: 3. ข้อมูลหลัก
 * Story: พนักงาน (Employee) เป็น master data ของงานเงินเดือน — ใช้คำนวณเงินเดือน,
 *        ภาษีหัก ณ ที่จ่าย (ภ.ง.ด.1), และประกันสังคม.
 *
 * Captured against /settings/employees (namespace 'employee'). ฟอร์มเพิ่ม
 * พนักงานเป็น modal (.modal-box). Persona: admin. co2 seed มีพนักงาน ~1 คน.
 *
 * หมายเหตุ: walkthrough เปิด modal เพิ่มพนักงานเพื่ออธิบายฟิลด์ แต่ไม่กรอก/บันทึก
 * (คู่มือเน้นอธิบายฟอร์ม; การคำนวณเงินเดือนจริงอยู่บทที่ 6).
 */
import { walkthrough } from '../lib/walkthrough';

walkthrough({
  id: '03.05',
  title: 'พนักงาน (สำหรับเงินเดือน)',
  chapter: '3. ข้อมูลหลัก',
  persona: 'admin',
  intro: `
พนักงาน (Employee) คือ master data ที่งานเงินเดือน (Payroll) ใช้คำนวณ:

- **เงินเดือน** + เงินได้อื่น
- **ภาษีหัก ณ ที่จ่าย (ภ.ง.ด.1)** — คำนวณจากเงินเดือน, สถานภาพสมรส, จำนวนบุตร,
  คู่สมรสมีเงินได้หรือไม่ (ค่าลดหย่อน)
- **ประกันสังคม (ม.33)** — ถ้าพนักงานอยู่ในระบบประกันสังคม

ฟอร์มเก็บ: รหัสพนักงาน, คำนำหน้า/ชื่อ/นามสกุล, เลขบัตรประชาชน (13 หลัก),
เงินเดือน, วันเริ่มงาน, สถานภาพสมรส + ค่าลดหย่อน, ข้อมูลธนาคาร (รับเงินเดือน),
และที่อยู่. ตั้งให้ครบเพื่อให้รอบเงินเดือน (บทที่ 6) คำนวณภาษีได้ถูกต้อง.
  `.trim(),
  prerequisites: [
    'login ในฐานะผู้มีสิทธิ์ master.employee.manage (admin)',
  ],
}, async ({ page, capture }) => {

  // ─── Step 1: employee list ───────────────────────────────────────────
  await page.goto('/settings/employees');
  await capture('step-01-list', {
    highlight: 'main',
    caption:
      'ขั้นที่ 1: หน้า "พนักงาน (Payroll)" — รายชื่อพนักงานที่ใช้คำนวณเงินเดือน.' +
      ' ปุ่ม "เพิ่มพนักงาน" เปิดฟอร์มกรอกข้อมูลใหม่',
  });

  // ─── Step 2: open add modal ──────────────────────────────────────────
  await page.getByRole('button', { name: 'เพิ่มพนักงาน' }).click();
  await page.locator('.modal-box').waitFor({ state: 'visible' });
  await capture('step-02-modal', {
    highlight: '.modal-box',
    caption:
      'ขั้นที่ 2: ฟอร์ม "เพิ่มพนักงาน" — คำนำหน้า/ชื่อ/นามสกุล, เลขบัตรประชาชน' +
      ' (13 หลัก), เงินเดือน, วันเริ่มงาน, สถานภาพสมรส + จำนวนบุตร (ค่าลดหย่อน' +
      ' ภ.ง.ด.1), ประกันสังคม (ม.33), และเลขบัญชีธนาคารสำหรับรับเงินเดือน.' +
      ' กรอกครบแล้วกด "บันทึก" — พนักงานจะถูกดึงเข้ารอบเงินเดือนอัตโนมัติ (บทที่ 6)',
  });

});
