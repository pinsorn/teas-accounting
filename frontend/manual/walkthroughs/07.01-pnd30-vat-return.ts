import { walkthrough } from '../lib/walkthrough';

walkthrough({
  id: '07.01',
  title: 'ภ.พ.30 ภาษีมูลค่าเพิ่ม — ภาษีขาย − ภาษีซื้อ',
  chapter: '7. ภาษี',
  persona: 'admin',
  intro: `
**ภ.พ.30** คือแบบแสดงรายการ **ภาษีมูลค่าเพิ่ม (VAT)** ที่กิจการจด VAT ต้องยื่น
**ทุกเดือน ภายในวันที่ 15 ของเดือนถัดไป** (§4.5):

- **ภาษีขาย (Output VAT)** — VAT 7% ที่เก็บจากลูกค้า รวมจาก **ใบกำกับภาษี/ใบเพิ่มหนี้**
  ที่ออกในงวด หัก **ใบลดหนี้**.
- **ภาษีซื้อ (Input VAT)** — VAT 7% ที่จ่ายให้ผู้ขาย รวมจาก **ใบกำกับภาษีซื้อ** ที่บันทึก
  (เคลมได้ตามงวดเครดิต ม.82/4 — ดู 05.02).
- **ภาษีที่ต้องชำระสุทธิ = ภาษีขาย − ภาษีซื้อ** (ถ้าติดลบ = **เครดิตยกไปงวดหน้า**).

หน้านี้แยก 2 ขั้น: **"แสดงตัวอย่าง"** = คำนวณจากบัญชีแยกประเภท (GL) ทันที ดูได้ไม่จำกัด;
**"ยืนยัน/ปิดงวด"** = ปิดงวดเพื่อนำส่ง (โหมด \`auto\` ยื่นผ่าน RD API / \`manual\` ยื่นเอง —
ตั้งระดับบริษัท §4.6). คู่มือนี้สาธิตเฉพาะ **แสดงตัวอย่าง** (อ่านอย่างเดียว).
  `.trim(),
  prerequisites: [
    'login admin (สิทธิ์ report.pnd30)',
    'กิจการจด VAT + มีใบกำกับภาษี/ภาษีซื้อ ในงวด (ดู บท 4–5)',
  ],
}, async ({ page, capture }) => {

  // ─── Step 1: the ภ.พ.30 page — period + actions ──────────────────────
  await page.goto('/reports/pnd30');
  await page.getByRole('button', { name: 'แสดงตัวอย่าง' }).waitFor({ state: 'visible' });
  await capture('step-01-page', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 1: หน้า "ภ.พ.30" — เลือก "งวด (เดือน/ปี)" แล้วกด "แสดงตัวอย่าง" เพื่อให้ระบบ' +
      ' คำนวณภาษีขาย/ภาษีซื้อของงวดนั้นจากบัญชีให้อัตโนมัติ. ปุ่ม "ยืนยัน/ปิดงวด" ใช้ตอนนำส่งจริง',
  });

  // ─── Step 2: preview — computed VAT return table (read-only) ──────────
  // เดือนปัจจุบันเป็นค่าเริ่มต้นของช่องงวดอยู่แล้ว (มีเอกสาร co2 ของงวดนี้).
  const month = new Date().toISOString().slice(0, 7); // yyyy-MM
  await page.locator('input[type=month]').fill(month);
  await page.getByRole('button', { name: 'แสดงตัวอย่าง' }).click();
  await page.getByText('ภาษีที่ต้องชำระสุทธิ').waitFor({ state: 'visible', timeout: 15_000 });
  await capture('step-02-preview', {
    highlight: 'main',
    caption:
      'ขั้นที่ 2: ผลการแสดงตัวอย่าง — ตารางสรุปทีละบรรทัด: ขายที่ต้องเสียภาษี · ขาย 0% ·' +
      ' ขายยกเว้น → รวมเป็น "ภาษีขายรวม"; ซื้อที่ขอคืนได้ + สัดส่วนเครดิต (ม.82/6) → "ภาษีซื้อรวม";' +
      ' บรรทัดล่างสุด "ภาษีที่ต้องชำระสุทธิ" = ภาษีขาย − ภาษีซื้อ. มีกำหนดยื่น + ชื่อ/เลขผู้เสียภาษีบริษัทกำกับ',
  });

});
