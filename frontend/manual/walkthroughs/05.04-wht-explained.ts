import { walkthrough } from '../lib/walkthrough';

walkthrough({
  id: '05.04',
  title: 'ภาษีหัก ณ ที่จ่าย (WHT) โดยละเอียด',
  chapter: '5. งานซื้อ',
  persona: 'admin',
  intro: `
**ภาษีหัก ณ ที่จ่าย (Withholding Tax / WHT)** คือภาษีที่ "ผู้จ่ายเงิน" หักจากผู้รับไว้
ส่วนหนึ่งแล้วนำส่งสรรพากรแทน พร้อมออกหนังสือรับรอง **50ทวิ** ให้ผู้รับ (ดู flow เต็มใน 05.03).

**อัตราหักขึ้นกับ "ประเภทเงินได้" (ม.40):**

| ประเภทเงินได้ | อัตรา | ตัวอย่าง |
|---|---|---|
| ค่าโฆษณา | 2% | ค่าโฆษณา ค่าประชาสัมพันธ์ |
| ค่าบริการ / ค่าจ้างทำของ | 3% | ค่าที่ปรึกษา ค่าซ่อม ค่าออกแบบ |
| ค่าเช่า (อสังหาฯ) | 5% | ค่าเช่าสำนักงาน/อาคาร |
| ค่าวิชาชีพอิสระ | 3% | ค่าทนายความ ค่าสอบบัญชี |

**แบบนำส่ง** ขึ้นกับผู้รับเงิน: นิติบุคคล = **ภ.ง.ด.53**, บุคคลธรรมดา = **ภ.ง.ด.3**.

**สำคัญ — เมื่อไหร่ "ไม่" หัก:** การซื้อ **สินค้า/ของ** ไม่ต้องหัก ณ ที่จ่าย (เลือก "ไม่หัก");
หักเฉพาะ **ค่าบริการ/ค่าเช่า/ค่าจ้าง** ฯลฯ. และยอดน้อยกว่าเกณฑ์ขั้นต่ำ (1,000 บาท) ก็ไม่ต้องหัก.

**ทั้งสองฝั่ง:** ฝั่ง **ซื้อ** (บทนี้) = เราหักจากผู้ขายตอนจ่าย (ใบสำคัญจ่าย → 50ทวิ);
ฝั่ง **ขาย** = ลูกค้าหักจากเรา → บันทึกตอนรับเงินในใบเสร็จ (ดู 04.05 ส่วน "หัก ณ ที่จ่าย").

**ผู้จ่ายออกภาษีแทน (gross-up):** ถ้าตกลงให้ผู้รับได้เต็มจำนวน ผู้จ่ายต้อง "ออกภาษีแทน"
แล้ว gross-up ยอดภาษีเข้าไปในเงินได้ของผู้รับ — ระบบมีตัวเลือกให้ (บังคับอัตโนมัติสำหรับ
ผู้ขายต่างประเทศที่ไม่มีเลข VAT ไทย).
  `.trim(),
  prerequisites: [
    'login admin (สิทธิ์ payment_voucher)',
    'มีประเภทเงินได้ 50ทวิ ในระบบ',
  ],
}, async ({ page, capture }) => {

  // ─── Step 1: PV line — the income-type column ────────────────────────
  await page.goto('/payment-vouchers/new');
  await page.getByRole('button', { name: /^เลือกผู้ขาย$|ค้นหาชื่อ หรือรหัสผู้ขาย/ }).first().click();
  const vDialog = page.getByRole('dialog');
  await vDialog.getByRole('textbox').fill('ออฟฟิศ');
  await vDialog.getByRole('button', { name: /ออฟฟิศ/ }).first().click();
  await page.getByTestId('expense-category-select').selectOption({ index: 5 }); // SVC
  await page.getByLabel('หน่วยธุรกิจ').selectOption({ index: 1 });
  await page.getByLabel('รายละเอียด 1').fill('ค่าบริการติดตั้งและบำรุงรักษาอุปกรณ์');
  await page.getByLabel(/มูลค่าก่อนภาษี/).first().fill('30000');
  await capture('step-01-form', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 1: ในแต่ละบรรทัดของใบสำคัญจ่ายมีช่อง "ประเภทเงินได้ (50ทวิ)" — เลือกให้' +
      ' ตรงกับลักษณะเงินที่จ่าย (ค่าบริการ/ค่าเช่า/ค่าโฆษณา ฯลฯ). ถ้าซื้อสินค้าให้เลือก "ไม่หัก"',
  });

  // ─── Step 2: pick ค่าบริการ (3%) → rate auto-filled ──────────────────
  await page.getByTestId('pv-line-wht-type').selectOption({ label: 'ค่าบริการ (3%)' });
  await capture('step-02-service-3pct', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 2: เลือกประเภท "ค่าบริการ (3%)" → ระบบใส่อัตรา 3% ให้อัตโนมัติ และหัก' +
      ' ณ ที่จ่าย 900 (3% ของ 30,000). ตัวอย่างเอกสารแสดง "จ่ายสุทธิ" = มูลค่า + VAT − 900',
  });

  // ─── Step 3: change to ค่าเช่า (5%) → rate differs by income type ────
  await page.getByTestId('pv-line-wht-type').selectOption({ label: 'ค่าเช่า (5%)' });
  await capture('step-03-rent-5pct', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 3: เปลี่ยนประเภทเป็น "ค่าเช่า (5%)" → อัตราหักเปลี่ยนเป็น 5% และยอดหักเป็น' +
      ' 1,500 ทันที. อัตราหัก "ขึ้นกับประเภทเงินได้" — เลือกผิดประเภท = หักผิดอัตรา',
  });

  // ─── Step 4: self-withhold (gross-up) toggle ─────────────────────────
  await page.getByRole('checkbox').first().check();
  await capture('step-04-self-withhold', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 4: เปิด "ผู้จ่ายออกภาษีแทน" สำหรับกรณีตกลงให้ผู้รับได้เต็มจำนวน — ระบบ' +
      ' gross-up ยอดภาษีเข้าไปในเงินได้ และให้เลือกวิธี (ออกแทนตลอดไป/ครั้งเดียว).' +
      ' บังคับอัตโนมัติสำหรับผู้ขายต่างประเทศที่ไม่มีเลข VAT ไทย. ผู้รับ (นิติบุคคล) → แบบ ภ.ง.ด.53',
  });

});
