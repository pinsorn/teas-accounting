/**
 * 06.01 — รอบจ่ายเงินเดือน (Payroll Run) — คำนวณภาษี + ประกันสังคมอัตโนมัติ
 *
 * Chapter: 6. เงินเดือน
 * Story: สร้าง "รอบจ่าย" ของงวดเดือน → ระบบดึงพนักงานที่ทำงานอยู่มาคำนวณ เงินเดือน,
 *        ภาษีหัก ณ ที่จ่าย (ภ.ง.ด.1), และประกันสังคม ให้อัตโนมัติ → อนุมัติ → บันทึกบัญชี
 *        → จ่าย. ออกเอกสาร ภ.ง.ด.1 / ปกส. / สลิป ได้หลังบันทึกบัญชี.
 *
 * Persona: admin (payroll.run.manage/.post/.pay).
 * Captured against /payroll. แสดง create modal + เปิดรอบจ่ายตัวอย่าง (งวด 202602 มี
 * พนักงานตัวอย่าง 2 คน) แบบ read-only — รอบจ่ายคงสถานะร่างไว้ให้ re-capture ได้.
 *
 * Data (co2): พนักงานตัวอย่าง MD-EMP-001 (เงินเดือน 35,000 โสด) + MD-EMP-002
 * (22,000 สมรส บุตร 2) → เห็นความต่างของภาษีตามสถานะครอบครัว.
 */
import { walkthrough } from '../lib/walkthrough';

walkthrough({
  id: '06.01',
  title: 'รอบจ่ายเงินเดือน + ภ.ง.ด.1 + ประกันสังคม',
  chapter: '6. เงินเดือน',
  persona: 'admin',
  intro: `
ระบบเงินเดือนทำงานเป็น **"รอบจ่าย" (Payroll Run)** ต่อหนึ่งงวดเดือน:

1. **สร้างรอบจ่าย** — ระบุงวดเดือน + วันที่จ่าย → ระบบ **ดึงพนักงานที่ทำงานอยู่ในงวดนั้น
   มาคำนวณให้อัตโนมัติ**: เงินเดือน, **ภาษีหัก ณ ที่จ่าย (PIT → ภ.ง.ด.1)**, และ
   **ประกันสังคม (5% ทั้งฝั่งลูกจ้าง/นายจ้าง สูงสุด 750 บาท/เดือน)**.
2. **อนุมัติ → บันทึกบัญชี → จ่าย** — รอบจ่ายไหลผ่านสถานะ ร่าง → อนุมัติแล้ว → บันทึกบัญชีแล้ว
   (ลงบัญชี + ล็อกถาวร) → จ่ายแล้ว.
3. **เอกสาร** — หลังบันทึกบัญชี ออก **ภ.ง.ด.1** (นำส่งภาษีพนักงาน), **ปกส.** (นำส่ง
   ประกันสังคม), **สลิปเงินเดือน** และ **ภ.ง.ด.1ก** (สรุปทั้งปี) ได้.

**ภาษีคิดตามสถานะบุคคล** — ลดหย่อนส่วนตัว/คู่สมรส/บุตร ทำให้พนักงานเงินเดือนเท่ากันแต่
สถานะครอบครัวต่างกัน เสียภาษีไม่เท่ากัน (ระบบคิดให้ตามข้อมูลพนักงาน ดู 03.05).
  `.trim(),
  prerequisites: [
    'login admin (สิทธิ์ payroll.run.manage/post/pay)',
    'มีพนักงานพร้อมเงินเดือน + สถานะภาษี ในระบบ (03.05)',
  ],
}, async ({ page, capture }) => {

  // ─── Step 1: payroll runs list ───────────────────────────────────────
  await page.goto('/payroll');
  await capture('step-01-list', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 1: หน้า "เงินเดือน" แสดงรอบจ่ายแต่ละงวด (สถานะ/จำนวนพนักงาน/ยอดรับสุทธิ).' +
      ' มุมขวามีปุ่ม "สร้างรอบจ่าย" และ "ภ.ง.ด.1ก (PDF)" สำหรับสรุปภาษีทั้งปี',
  });

  // ─── Step 2: create-run modal ────────────────────────────────────────
  await page.getByRole('button', { name: 'สร้างรอบจ่าย' }).first().click();
  const modal = page.locator('.modal-box');
  await modal.waitFor({ state: 'visible' });
  await page.getByLabel('งวดเดือน').fill('202602');
  await capture('step-02-create-modal', {
    highlight: '.modal-box',
    caption:
      'ขั้นที่ 2: หน้าต่าง "สร้างรอบจ่าย" — ใส่ "งวดเดือน" (YYYYMM) + "วันที่จ่าย".' +
      ' เมื่อกดสร้าง ระบบจะดึงพนักงานที่ทำงานอยู่ในงวดนั้นมาคำนวณภาษี + ประกันสังคมให้เอง',
  });
  await page.keyboard.press('Escape');

  // ─── Step 3: an existing run — auto-computed payslips + totals ────────
  const res = await page.request.get('/api/proxy/payroll/runs');
  const body = await res.json();
  const runs = Array.isArray(body) ? body : (body.items ?? []);
  const run = runs.find((r: { periodYearMonth: string | number }) => String(r.periodYearMonth) === '202602');
  await page.goto(`/payroll/${run.payrollRunId}`);
  await page.getByText('รายการพนักงาน').waitFor({ state: 'visible', timeout: 10_000 });
  await capture('step-03-run-detail', {
    highlight: 'main',
    caption:
      'ขั้นที่ 3: รอบจ่ายตัวอย่าง — การ์ดสรุปด้านบน: รวมเงินได้ · ภาษีหัก ณ ที่จ่าย ·' +
      ' ประกันสังคม · รับสุทธิ. ด้านล่างเป็นรายการพนักงานที่ระบบคำนวณให้ทีละคน' +
      ' (เงินเดือนเท่ากันแต่ภาษีต่างกันตามสถานะครอบครัว)',
  });

  // ─── Step 4: lifecycle actions ───────────────────────────────────────
  await capture('step-04-actions', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 4: รอบจ่ายสถานะ "ร่าง" — กด "อนุมัติ" แล้ว "บันทึกบัญชี" (ล็อกถาวร) แล้ว' +
      ' "จ่ายแล้ว". หลังบันทึกบัญชีจะมีปุ่มออก ภ.ง.ด.1 / ปกส. / สลิปเงินเดือน ให้นำส่งและแจกพนักงาน',
  });

});
