/**
 * 04.03 — ออกใบแจ้งหนี้ / ใบวางบิล (Billing Note)
 *
 * Chapter: 4. งานขาย
 * Story: ออกใบแจ้งหนี้เพื่อเรียกเก็บเงินจากลูกค้า → เลือกลูกค้า + หน่วยธุรกิจ +
 *        รายการ → "ออกใบแจ้งหนี้". ใบแจ้งหนี้เป็นเอกสารเรียกเก็บเงิน ใช้รวมหลาย
 *        ใบกำกับภาษีเข้าด้วยกันเพื่อวางบิลครั้งเดียว หรือออกพร้อมรายการของตัวเองได้.
 *
 * Persona: admin (sales.billing_note.create + .send).
 * Captured against /invoices/new (BillingNoteForm = PartySelectBox +
 * BusinessUnitSelector + LineItemsTable + ตัวเลือกจัดกลุ่มใบกำกับภาษี).
 *
 * Data (co2): ลูกค้า "บริษัท แอคมี จำกัด", หน่วยธุรกิจ ECOM.
 * ⚠️ สร้างเอกสารจริงบน company 2 (ออกเลขที่เอกสาร).
 */
import { walkthrough } from '../lib/walkthrough';

walkthrough({
  id: '04.03',
  title: 'ออกใบแจ้งหนี้ / ใบวางบิล',
  chapter: '4. งานขาย',
  persona: 'admin',
  intro: `
ใบแจ้งหนี้ / ใบวางบิล (Billing Note) คือเอกสาร **เรียกเก็บเงิน** ที่ส่งให้ลูกค้าเพื่อ
แจ้งยอดที่ต้องชำระและกำหนดวันครบกำหนด.

**ใช้งานได้ 2 แบบ:**

1. **รวมใบกำกับภาษีหลายใบ** — กิจการที่ส่งของหลายครั้งในเดือน แล้ววางบิลรวมครั้งเดียว
   สามารถเลือกใบกำกับภาษีที่ออกไว้แล้วมารวมในใบแจ้งหนี้ใบเดียว.
2. **ออกพร้อมรายการของตัวเอง** — กรอกรายการสินค้า/บริการลงในใบแจ้งหนี้โดยตรง
   (เหมือนใบเสนอราคา) ตามที่บทนี้แสดง.

เมื่อลูกค้าชำระเงิน จะออก **ใบเสร็จรับเงิน** อ้างอิงใบแจ้งหนี้/ใบกำกับภาษีนี้ (ดู 04.05).
มีช่อง "วันที่ครบกำหนด" (ค่าเริ่มต้น +30 วัน) เพื่อติดตามลูกหนี้.
  `.trim(),
  prerequisites: [
    'login admin (สิทธิ์ billing_note)',
    'มีลูกค้า + หน่วยธุรกิจ ในระบบ',
  ],
}, async ({ page, capture }) => {

  // ─── Step 1: blank form ──────────────────────────────────────────────
  await page.goto('/invoices/new');
  await capture('step-01-form', {
    highlight: 'main',
    caption:
      'ขั้นที่ 1: ฟอร์ม "สร้างใบแจ้งหนี้" — มีส่วนลูกค้า, ข้อมูลเอกสาร (วันที่ +' +
      ' วันที่ครบกำหนด), รายการ และตัวเลือกจัดกลุ่มใบกำกับภาษี พร้อมตัวอย่างเอกสารด้านขวา',
  });

  // ─── Step 2: pick customer ───────────────────────────────────────────
  await page.getByRole('button', { name: /^เลือกลูกค้า$|ค้นหาชื่อ หรือเลขผู้เสียภาษี/ }).first().click();
  const custDialog = page.getByRole('dialog');
  await custDialog.getByRole('textbox').fill('แอคมี');
  await custDialog.getByRole('button', { name: /แอคมี/ }).first().click();
  await page.getByLabel('หน่วยธุรกิจ').selectOption({ index: 1 });
  await capture('step-02-customer', {
    highlight: 'main',
    caption:
      'ขั้นที่ 2: เลือกลูกค้า "บริษัท แอคมี จำกัด" + หน่วยธุรกิจ. "วันที่ครบกำหนด"' +
      ' ตั้งค่าเริ่มต้นเป็น +30 วัน ใช้ติดตามลูกหนี้ที่ยังไม่ชำระ',
  });

  // ─── Step 3: add a line item ─────────────────────────────────────────
  await page.getByLabel('รายละเอียด 1').fill('ค่าบริการดูแลระบบรายเดือน (มิ.ย. 2569)');
  await page.getByLabel('จำนวน 1').fill('1');
  await page.getByLabel('ราคา/หน่วย 1').fill('25000');
  await capture('step-03-line', {
    highlight: 'main',
    arrow: 'up',
    caption:
      'ขั้นที่ 3: กรอกรายการ — "ค่าบริการดูแลระบบรายเดือน" 25,000. กล่องสรุป' +
      ' คำนวณมูลค่าก่อนภาษี + ภาษีมูลค่าเพิ่ม 7% = ยอดเรียกเก็บรวม อัตโนมัติ',
  });

  // ─── Step 4: issue → detail ──────────────────────────────────────────
  await page.getByRole('button', { name: 'ออกใบแจ้งหนี้' }).click();
  await page.waitForURL(/\/invoices\/\d+/, { timeout: 15_000 });
  await capture('step-04-issued', {
    highlight: 'main',
    caption:
      'ขั้นที่ 4: กด "ออกใบแจ้งหนี้" → ระบบออกเลขที่เอกสารและบันทึกใบแจ้งหนี้สถานะ' +
      ' "ออกแล้ว". จากหน้านี้พิมพ์ PDF ส่งลูกค้า, กด "ออกใบกำกับภาษี" เพื่อออกเอกสารภาษี,' +
      ' หรือ "ยืนยันชำระเงินครบแล้ว" เมื่อได้รับเงิน (การออกใบเสร็จ ดู 04.05)',
  });

});
