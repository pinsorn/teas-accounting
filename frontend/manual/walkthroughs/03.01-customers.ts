/**
 * 03.01 — สร้างลูกค้า (master data)
 *
 * Chapter: 3. ข้อมูลหลัก
 * Story: ก่อนออกใบเสนอราคา/ใบกำกับภาษี ต้องมีลูกค้าใน master ก่อน. หน้านี้
 *        แสดงรายการลูกค้า + ฟอร์มสร้างลูกค้าใหม่ (full-page).
 *
 * Captured against /customers + /customers/new (CustomerForm, namespace 'cust').
 * Persona: admin (master-data setup เป็นงานผู้ดูแล). co2 seed มีลูกค้า ~8 ราย.
 *
 * หมายเหตุ: walkthrough นี้กรอกตัวอย่าง 2 ช่อง (รหัส + ชื่อ) เพื่อโชว์ฟอร์มใช้งาน
 * จริง แต่ไม่กดบันทึก (คู่มือเน้นอธิบายฟอร์ม ไม่สร้าง data ค้างใน demo).
 */
import { walkthrough } from '../lib/walkthrough';

walkthrough({
  id: '03.01',
  title: 'สร้างลูกค้า',
  chapter: '3. ข้อมูลหลัก',
  persona: 'admin',
  intro: `
ลูกค้า (Customer) คือ master data ที่ใช้เป็น "ผู้ซื้อ" ในทุกเอกสารขาย
(ใบเสนอราคา → ใบกำกับภาษี → ใบเสร็จ). ต้องสร้างลูกค้าก่อนจึงจะออกเอกสารให้ได้.

ฟอร์มแบ่งเป็น 4 ส่วน:

- **ข้อมูลทั่วไป** — รหัสลูกค้า*, ชื่อ (ไทย)*, ชื่อ (อังกฤษ), ประเภท (บุคคลธรรมดา / นิติบุคคล)
- **ข้อมูลภาษี** — จดทะเบียน VAT, เลขประจำตัวผู้เสียภาษี, รหัสสาขา
- **ที่อยู่ / ผู้ติดต่อ** — ที่อยู่ออกใบกำกับ, ผู้ติดต่อ, โทรศัพท์, อีเมล
- **เงื่อนไขการค้า** — วงเงินเครดิต, เครดิตเทอม, สกุลเงิน

**สำคัญ (กฎหมาย ม.86/4):** ถ้าลูกค้าจดทะเบียน VAT ต้องกรอก **เลขประจำตัวผู้เสียภาษี
(13 หลัก)** + **รหัสสาขา (5 หลัก, 00000 = สำนักงานใหญ่)** เพราะข้อมูลนี้ต้องพิมพ์
ลงบนใบกำกับภาษีของผู้ซื้อที่เป็นผู้ประกอบการ VAT.
  `.trim(),
  prerequisites: [
    'login ในฐานะผู้มีสิทธิ์ master.customer.manage (admin)',
  ],
}, async ({ page, capture }) => {
  const suffix = Date.now().toString(36).toUpperCase().slice(-5);

  // ─── Step 1: customer list ───────────────────────────────────────────
  await page.goto('/customers');
  await capture('step-01-list', {
    highlight: 'table',
    caption:
      'ขั้นที่ 1: หน้า "ลูกค้า" — รายการลูกค้าทั้งหมด. คอลัมน์: รหัส, ชื่อ,' +
      ' ประเภท, เลขผู้เสียภาษี, สถานะ. ปุ่ม "เพิ่มลูกค้า" มุมขวาบนเปิดฟอร์มสร้างใหม่',
  });

  // ─── Step 2: create form overview ────────────────────────────────────
  await page.goto('/customers/new');
  await page.getByLabel('รหัสลูกค้า').waitFor({ state: 'visible' });
  await capture('step-02-form', {
    highlight: 'main',
    caption:
      'ขั้นที่ 2: ฟอร์ม "เพิ่มลูกค้าใหม่" — 4 ส่วน: ข้อมูลทั่วไป, ข้อมูลภาษี,' +
      ' ที่อยู่/ผู้ติดต่อ, เงื่อนไขการค้า. ช่องที่มี * คือบังคับ (รหัส + ชื่อไทย)',
  });

  // ─── Step 3: fill identity fields (no save — demo only) ──────────────
  await page.getByLabel('รหัสลูกค้า').fill(`MC-${suffix}`);
  await page.getByLabel('ชื่อ (ไทย)').fill(`บริษัท ตัวอย่าง ${suffix} จำกัด`);
  await capture('step-03-fill', {
    highlight: 'main',
    caption:
      `ขั้นที่ 3: กรอกข้อมูลทั่วไป — รหัส "MC-${suffix}" + ชื่อไทย. ลูกค้าที่` +
      ' จดทะเบียน VAT (สวิตช์ "จดทะเบียน VAT" ในส่วนข้อมูลภาษี เปิดเป็นค่าตั้งต้น)' +
      ' ต้องกรอกเลขผู้เสียภาษี 13 หลัก + รหัสสาขา (ม.86/4). เสร็จแล้วกด "บันทึก"' +
      ' ลูกค้าจะพร้อมใช้ในเอกสารขายทันที',
  });

});
