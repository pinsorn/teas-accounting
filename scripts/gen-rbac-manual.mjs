// node scripts/gen-rbac-manual.mjs — reads the e2e results JSON + screenshots, writes the manual.
// One combined doc: a feature×role visibility matrix per company-type (VAT / non-VAT),
// a super-only appendix, and a per-role sidebar screenshot gallery. Deterministic:
// the matrices are computed from the recorded `expected` (the gating rule), never hand-edited.
import { readFileSync, writeFileSync, existsSync, readdirSync, mkdirSync } from 'node:fs';

const RESULTS = 'frontend/e2e/.artifacts/rbac-gating-results.json';
const OUT = 'docs/manual/rbac-ui-guide.md';

const results = JSON.parse(readFileSync(RESULTS, 'utf8'));

// Preserve manifest order (first appearance) for both axes.
const roles = [...new Set(results.map((r) => r.role))];
const features = [...new Set(results.map((r) => r.feature))];

const cell = (company, role, feature) => {
  const row = results.find((r) => r.company === company && r.role === role && r.feature === feature);
  return row ? (row.expected ? '✓' : '') : '–';
};
const matrix = (company, title) => {
  let s = `\n### ${title}\n\n| Feature | ${roles.join(' | ')} |\n|---|${roles.map(() => '---').join('|')}|\n`;
  for (const f of features) s += `| ${f} | ${roles.map((r) => cell(company, r, f)).join(' | ')} |\n`;
  return s;
};

const shots = existsSync('frontend/e2e/screenshots/rbac')
  ? readdirSync('frontend/e2e/screenshots/rbac').filter((f) => f.endsWith('.png')).sort()
  : [];

let md = `# TEAS — RBAC UI Guide (who sees what)\n\n`;
md += `> GENERATED by \`scripts/gen-rbac-manual.mjs\` from the \`rbac-ui-gating\` e2e run. Do not hand-edit the matrices.\n`;
md += `> ✓ = the control is shown to that role · blank = hidden · SUPER_ADMIN sees everything (flag bypass). VAT-only features are hidden on a non-VAT company.\n\n`;
md += `## How gating works\n\n`;
md += `The sidebar and each action button are shown only when the signed-in user's role holds the matching permission (super-admins bypass). This is a **UX affordance** — it stops a user opening a form that will 403 on submit. It is **not** the security boundary: the backend enforces the same role×endpoint rule on every request (\`RbacCartesianTests\`, green ×2). A non-VAT company additionally hides ใบกำกับภาษี / ใบลดหนี้-เพิ่มหนี้ / ภ.พ.30 (ม.86, no VAT documents).\n`;
md += `\n## Visibility matrix — VAT company\n${matrix('vat', 'All features × roles (VAT company)')}`;
md += `\n## Visibility matrix — non-VAT company\n${matrix('nonvat', 'All features × roles (non-VAT company)')}`;

md += `\n## Super-admin-only / backend-enforced-only (not in the matrix)\n\n`;
md += `These controls are intentionally not asserted by the FE matrix above; their gating is proven by the backend tests and (where applicable) the super-only nav rows:\n\n`;
md += `- **Paid-up capital card** (\`/settings/company\`) — \`master.company.manage\`, SUPER_ADMIN only (§4.6 tax/company master data). It self-hides via an async \`GET /companies\`; same gate as the "Companies (tax cfg)" nav row above.\n`;
md += `- **Document-detail lifecycle actions** — approve / post / cancel / settle on Payment Voucher, Tax Invoice, Purchase Order, Vendor Invoice, and Payroll detail pages. Each carries its OWN distinct \`PermissionGate\` scope (Sprint 13k Plan 2 Phase E) and is independently enforced on the backend (\`RbacCartesianTests\`). **These are NOT yet asserted by the matrix above** — they need a seeded document in the right status per company; planned as the follow-up extension to this e2e (open question for Ham). Until then their who-sees-what is proven only at the backend boundary.\n`;
md += `- **In-form Save** on \`/new\` create forms — reached only through a gated create button (shown above) and re-checked by the backend on submit.\n`;
md += `- **settings/* create buttons** — the settings page itself is nav-gated by the same \`*.manage\` permission, so the page (and its create button) is hidden as a unit.\n`;

md += `\n## Screenshots (sidebar per role)\n\n`;
md += `Each image is the dashboard sidebar as that role sees it, on each company type.\n\n`;
for (const s of shots) md += `### ${s.replace('.png', '')}\n\n![${s}](../../frontend/e2e/screenshots/rbac/${s})\n\n`;

mkdirSync('docs/manual', { recursive: true });
writeFileSync(OUT, md);
console.log(`wrote ${OUT} (${features.length} features × ${roles.length} roles, ${shots.length} screenshots)`);
